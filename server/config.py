"""服务端配置 — 路径常量 + 运行参数集中管理"""

import os

# ============================================================
# 项目路径
# ============================================================

# 项目根目录
PROJECT_ROOT = os.path.dirname(os.path.dirname(os.path.abspath(__file__)))

# 前端静态文件目录
WEB_DIR = os.path.join(PROJECT_ROOT, "web")

# SQLite 数据库文件
DB_PATH = os.path.join(PROJECT_ROOT, "assistant.db")

# 截图保存目录（自动作为静态文件被前端访问）
SCREENSHOT_DIR = os.path.join(WEB_DIR, "screenshots")

# hprof dump 文件保留目录（供用户下载）
DUMPS_DIR = os.path.join(WEB_DIR, "dumps")

# ============================================================
# 会话压缩参数
# ============================================================

# Token 计数阈值，超过时触发历史对话压缩
COMPRESS_THRESHOLD = 8000

# 压缩时保留的最近轮次数（不参与压缩）
KEEP_RECENT_TURNS = 6

# ============================================================
# 趋势数据缓存
# ============================================================

# 环形缓冲区最大数据点数（5 分钟 / 2 秒 = 150 个点）
TREND_BUFFER_SIZE = 150

# ============================================================
# SSE 实时指标采集间隔（秒）
# ============================================================

METRICS_INTERVAL = 2

# 防重叠最小间隔（秒）
METRICS_MIN_INTERVAL = 1.5

# ============================================================
# Agent 运行参数
# ============================================================

# Agent 最大推理轮次
AGENT_MAX_ITERATIONS = 15


def ensure_directories():
    """确保所有运行时需要的目录存在"""
    for d in [SCREENSHOT_DIR, DUMPS_DIR]:
        os.makedirs(d, exist_ok=True)
